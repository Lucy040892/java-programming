package day18_conditions_practice_strings_intro;

public class EvenOrOdd {
    public static void main(String[] args) {
        int num = 4;
        if(num % 2 == 0){ //divisible by 2
            System.out.println(num+ " Num is even");
        }else{
            System.out.println(num+ " Num is odd");
        }
    }
}
